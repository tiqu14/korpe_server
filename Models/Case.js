const mongoose = require("mongoose");

const CaseSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    total: {
      type: Number,
      required: true,
    },
  },
  { timestamps: true }
);

const Case = mongoose.model("Case", CaseSchema);

module.exports = Case;